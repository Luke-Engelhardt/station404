import random


def placeTraps(spaceship):
    for i in range(len(spaceship)):
        for j in range(len(spaceship)):
            if random.randint(1, 7) == 1:
                spaceship[i][j] = 1
    return spaceship
    
def scanField(x, y, spaceship):
    if spaceship[x][y] == 1:
        return "Trap"
    else:
        return "Safe"

def markField(x, y, spaceship):
    spaceship[x][y] = 2
    return spaceship

def getTraps(spaceship):
    traps = []
    for i in range(len(spaceship)):
        for j in range(len(spaceship)):
            if spaceship[i][j] == 1:
                traps.append((i, j))
    return traps
    
def checkWin(spaceship, solution):
    for trap in getTraps(solution):
        if spaceship[trap[0]][trap[1]] != 2:
            return False
    return True
  
